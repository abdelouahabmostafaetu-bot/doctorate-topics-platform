function attribution(author: string, url: string) {
  return `**المصدر والترخيص:** إعداد عربي محرر مستند إلى إجابة **${author}**. [رابط النص الأصلي](${url}). النص الأصلي وهذه الصياغة المشتقة مرخّصان بموجب [CC BY-SA 4.0](https://creativecommons.org/licenses/by-sa/4.0/).`;
}

/** يحوّل النص إلى بنية عربية هادئة وواضحة للقارئ. */
function formatForArabicReading(content: string) {
  const marker = "\n\n**المصدر والترخيص:**";
  const markerIndex = content.indexOf(marker);
  const body = markerIndex === -1 ? content : content.slice(0, markerIndex);
  const source = markerIndex === -1 ? "" : content.slice(markerIndex + 2);
  const paragraphs = body.split("\n\n").filter(Boolean);
  const [mainIdea, ...practicalPoints] = paragraphs;

  return [
    "## الفكرة الرئيسة",
    mainIdea,
    "## نقاط عملية للقارئ",
    ...practicalPoints.map((point, index) => `${index + 1}. ${point}`),
    source,
  ]
    .filter(Boolean)
    .join("\n\n");
}

